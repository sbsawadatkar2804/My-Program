package Operators;

public class arithematicOperator {
	
	public static void main(String[] args) {
		
		int a=10, b=20;
	
		// Additional Operators
		
		System.out.println("Sum Of A & B, " + "a+b= "+ (a+b));
		
		// Module Operators
		
		System.out.println("Remender of A & B, " + "a%b= " + (a%b));
		
		// Division Operators
		
		System.out.println("Division of A & B, " + "a/b=" + (a/b));
		
		//Multiplication Operators
		
		System.out.println("Multiplications Of A & B, " + "a*b=" + (a*b));
		
		// subtraction Operators
		
		System.out.println("Subtraction of A & B, "+"a-b=" + (a-b));
		
	//	int a = 125, b = 50;
		
		// int report1, report2;
		
		 // original value
		
		System.out.println("value of a = "+ a);
		
		//Increment operator
		
		System.out.println("after increment = " + a++);
		
		// original value
		
		System.out.println("value of b = "+ b);
		
		// decrement operator
		
		System.out.println("After decrement b = "+ b--);
		
		// Post Increment And Decrement Operator
		
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(--b);
		System.out.println(b--);
				
	}

}
